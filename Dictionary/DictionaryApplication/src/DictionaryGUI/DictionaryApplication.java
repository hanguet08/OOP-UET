    private void targetKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_targetKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            String s = target.getText();
            String text = dictionary.searchWord(s);
            if (text == null) {
                try {
                    jTextField1.setText(s);
                    String r = translator.callUrlAndParseResult("en", "vi", jTextField1.getText());
                    if (!"".equals(r)) {
                        VietnameseBox.setText(r);
                        switchTab(GoogleAPI);
                    }
                } catch (Exception e) {
                    e.getMessage();
                }
                targetBox.setText("No Matches were found.");
                explain.setText(" ");
            } else {
                explain.setText(text);
                targetBox.setText(s);
                switchTab(Database);
            }

        }
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {

            int t = autocomplete.getSelectedIndex();
            if (t == -1) {
                autocomplete.setSelectedIndex(0);
            } else if (t == dlm.size()) {
                autocomplete.setSelectedIndex(0);
            } else {
                autocomplete.setSelectedIndex(t + 1);
            }
            target.setText(autocomplete.getSelectedValue());
        }
        if (evt.getKeyCode() == KeyEvent.VK_UP) {

            int t = autocomplete.getSelectedIndex();
            if (t == -1) {
                autocomplete.setSelectedIndex(dlm.size() - 1);
            } else if (t == 0) {
                autocomplete.setSelectedIndex(dlm.size() - 1);
            } else {
                autocomplete.setSelectedIndex(t - 1);
            }
            target.setText(autocomplete.getSelectedValue());
        }
    }//GEN-LAST:event_targetKeyPressed

