    private void autocompleteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_autocompleteKeyPressed
        // TODO add your handling code here:
        int t = autocomplete.getSelectedIndex();
        if (evt.getKeyCode() == KeyEvent.VK_UP) {
            if (t > 0) {
               
                String s = autocomplete.getSelectedValue();
                targetBox.setText(s);
                explain.setText(dictionary.searchWord(s));
                autocomplete.setSelectedIndex(t - 1);
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            if (t < autocomplete.getMaxSelectionIndex()) {
                
                String s = autocomplete.getSelectedValue();
                targetBox.setText(s);
                explain.setText(dictionary.searchWord(s));
                autocomplete.setSelectedIndex(t + 1);
            }
        }
    }//GEN-LAST:event_autocompleteKeyPressed

